    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if ((!jCheckBox1.isSelected() && (!jCheckBox2.isSelected()))) {
            JOptionPane.showMessageDialog(null, "Selecione uma família antes de validar a versão.", "Atenção!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String familia = "";
        if (jCheckBox1.isSelected()) {
            familia = "fPG5";
        } else if (jCheckBox2.isSelected()) {
            familia = "fSG5";
        }

        if (mainController.checkVersion(jTextField1.getText(), familia)) {
            JOptionPane.showMessageDialog(null, "Versão validada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Versão não encontrada", "Erro!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

