    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<String> copyOperations = new ArrayList<String>();
        if (jCheckBox1.isSelected()) {
            copyOperations.add("PG5");
        } else if (jCheckBox2.isSelected()) {
            if (jCheckBox3.isSelected()) {
                copyOperations.add("SG5");
            }
            if (jCheckBox4.isSelected()) {
                copyOperations.add("INDSG5");
            }
            if (jCheckBox5.isSelected()) {
                copyOperations.add("PROSG5");
            }
            if (jCheckBox7.isSelected()) {
                copyOperations.add("ADMSG5");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma família de sistema e os sistemas desejados antes de atualizar.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            jButton1.setEnabled(true);
            return;
        }
        CopyThread thread = new CopyThread(mainController, jTextField1.getText(), copyOperations);
        this.copyThread = thread;
        jButton1.setEnabled(true);
    }//GEN-LAST:event_jButton1ActionPerformed

